    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        int autor_no = Integer.parseInt(txtAutor_no.getText());
        String autor_name = txtAutor_name.getText();
        String gen_no = txtGen_no.getText();
        
       Autor a = new Autor();
        a.setAutor_name(autor_name);
        a.setGen_no(gen_no);
        a.setAutor_no(autor_no);
        
        AutorDAO autorDAO = new AutorDAO();
        autorDAO.alterar(a);
        
        txtAutor_name.setText("");
        txtGen_no.setText("");
        txtAutor_no.setText("");
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
      int idPesquisa = Integer.parseInt(txtIDPesquisar.getText());
        AutorDAO autorDAO = new AutorDAO();
        Autor a = autorDAO.getAutor(idPesquisa);
        if(a == null){
            JOptionPane.showMessageDialog(this, "Produto não encontrado");
        }else{
            txtAutor_no.setText(String.valueOf(a.getAutor_no()));
            txtAutor_name.setText(a.getAutor_name());
            txtGen_no.setText(a.getGen_no());
    }//GEN-LAST:event_btnPesquisarActionPerformed
    }
