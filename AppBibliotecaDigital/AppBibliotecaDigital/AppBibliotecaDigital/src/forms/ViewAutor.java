    private void btnCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarActionPerformed
     String autor_name = txtAutor_name.getText();
     String gen_no = txtGen_no.getText();
        int autor_no = Integer.parseInt(txtAutor_no.getText());
       
        Autor a = new Autor();
        a.setAutor_name(autor_name);
        a.setGen_no(gen_no);
        a.setAutor_no(autor_no);
        
        AutorDAO autorDAO = new AutorDAO();
        autorDAO.inserir(a);
        
        txtAutor_name.setText("");
        txtGen_no.setText("");
        txtAutor_no.setText("");
    }//GEN-LAST:event_btnCriarActionPerformed

