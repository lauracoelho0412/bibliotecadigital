    private void btnCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarActionPerformed
         String editora_name = txtEditora_name.getText();
         String autor_name = txtAutor_no.getText();
         String book_no = txtBook_no.getText();
        int editora_no = Integer.parseInt(txtEditora_no.getText());
        
        Editora e = new Editora();
        e.setEditora_name(editora_name);
        e.setAutor_name(autor_name);
        e.setBook_no(book_no);
        e.setEditora_no(editora_no);
        
        EditoraDAO editoraDAO = new EditoraDAO();
        editoraDAO.inserir(e);
        
        txtEditora_name.setText("");
        txtAutor_no.setText("");
        txtBook_no.setText("");
        txtEditora_no.setText("");
    }//GEN-LAST:event_btnCriarActionPerformed

