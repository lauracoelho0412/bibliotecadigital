    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
         int idPesquisa = Integer.parseInt(txtIDPesquisar.getText());
        EditoraDAO editoraDAO = new EditoraDAO();
        Editora e = editoraDAO.getEditora(idPesquisa);
        if(e == null){
            JOptionPane.showMessageDialog(this, "Editora não encontrada");
        }else{
            txtEditora_no.setText(String.valueOf(e.getEditora_no()));
            txtEditora_name.setText(e.getEditora_name());
            txtBook_no.setText(e.getBook_no());
            txtAutor_no.setText(e.getAutor_name());
           
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

