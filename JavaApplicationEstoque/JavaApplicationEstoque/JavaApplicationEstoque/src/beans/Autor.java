
package beans;


public class Autor {
    private String autor_name;
    private String gen_no;
    private int autor_no;

    public String getAutor_name() {
        return autor_name;
    }

    public void setAutor_name(String autor_name) {
        this.autor_name = autor_name;
    }

    public String getGen_no() {
        return gen_no;
    }

    public void setGen_no(String gen_no) {
        this.gen_no = gen_no;
    }

    public int getAutor_no() {
        return autor_no;
    }

    public void setAutor_no(int autor_no) {
        this.autor_no = autor_no;
    }

}
